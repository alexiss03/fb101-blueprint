// List of specials literals
enum NYOperation: String {
    
    case Add = "+"
    case Subt = "-"
    case Mult = "*"
    case Div = "/"
    case Pow = "^"
    case OpenPar = "("
    case ClosePar = ")"
    case SemiCol = ";"
    case Col = ":"
    case Equals = "="
    case Greater = ">"
    case Less = "<"
    case Concat = "&"
    
    
    case Max = "MAX"
    case Min = "MIN"
    case Sum = "SUM"
    case CountIf = "COUNTIF"
    case Npv = "NPV"
    
    
    case RDown = "ROUNDDOWN"
    case RUp = "ROUNDUP"
    case If = "IF"
    case Or = "OR"
    case And = "AND"
    case Index = "INDEX"
    case Text = "TEXT"
    
}
